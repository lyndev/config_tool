    private void setDefaultRightComponent()
    {
        if (jSplitPane2.getTopComponent().getName() == null)
            jSplitPane2.setTopComponent(getExcelPanel());
        jSplitPane2.setDividerLocation(jSplitPane2.getHeight() - 170);
    }

    private ExcelPanel getExcelPanel()
    {
        if (excelPanel == null)
            excelPanel = new ExcelPanel(consoleOutTextArea);
        return excelPanel;
    }

    private JDialog getOptionDialog()
    {
        if (optionDialog == null)
            optionDialog = new OptionDialog(this, true);
        return optionDialog;
    }

    private JDialog getAboutDialog()
    {
        if (aboutDialog == null)
            aboutDialog = new AboutDialog(this, true);
        return aboutDialog;
    }

    private void setConsoleOut()
    {
        PrintStream consoleStream = new PrintStream(new OutputStream()
        {
            @Override
            public void write(int b)
            {
            }

            @Override
            public void write(byte[] b, int off, int len)
            {
                consoleOutTextArea.append(new String(b, off, len));
            }
        });
        System.setOut(consoleStream);
        System.setErr(consoleStream);
    }

    private void promptClose()
    {
        if (JOptionPane.showConfirmDialog(this, "确认退出？", "退出", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == YES_OPTION)
            System.exit(1);
    }

