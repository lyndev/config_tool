    private void jTree1ValueChanged(javax.swing.event.TreeSelectionEvent evt)//GEN-FIRST:event_jTree1ValueChanged
    {//GEN-HEADEREND:event_jTree1ValueChanged
        // TODO add your handling code here:
        //System.out.println(jTree1.getSelectionModel().toString());
        TreePath path = jTree1.getSelectionPath();
        String nodeName = ((DefaultMutableTreeNode) path.getLastPathComponent()).getUserObject().toString();
        switch (nodeName)
        {
            case "Excel导入":
            {
                jSplitPane2.setTopComponent(getExcelPanel());
                break;
            }
            default:
            {
                log.error("unknow node [" + nodeName + "]");
            }
        }
        jSplitPane1.setDividerLocation(220);
        jSplitPane2.setDividerLocation(jSplitPane2.getHeight() - 170);
    }//GEN-LAST:event_jTree1ValueChanged

