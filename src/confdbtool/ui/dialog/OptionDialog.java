    private void formComponentShown(java.awt.event.ComponentEvent evt)//GEN-FIRST:event_formComponentShown
    {//GEN-HEADEREND:event_formComponentShown
        OptionConfigure config = OptionConfigure.getInstance();
        if (config.hasConfig())
        {
            excelFilePathTextField.setText(config.getExcelPath());
            jTextField2.setText(config.getMysqlUrl());
            jTextField3.setText(config.getMysqlUser());
            jTextField4.setText(config.getMysqlPasswd());
            javaCodePathTextField.setText(config.getJavaCodePath());
            luaCodePathTextField.setText(config.getLuaCodePath());
            this.JSFilePathTextField.setText(config.getJSCodePath());
            this.CSharpCodePathTextField.setText(config.getCSharpCodePath());
        }
    }//GEN-LAST:event_formComponentShown

