    private void chooseExcelDirButtonActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_chooseExcelDirButtonActionPerformed
    {//GEN-HEADEREND:event_chooseExcelDirButtonActionPerformed
        String pathString = excelFilePathTextField.getText();
        if (!pathString.isEmpty())
            getFileChooser().setSelectedFile(new File(pathString));
        if (getFileChooser().showOpenDialog(this) == APPROVE_OPTION)
        {
            String selectedPath = getFileChooser().getSelectedFile().getPath();
            OptionConfigure.getInstance().setExcelPath(selectedPath);
            excelFilePathTextField.setText(selectedPath);
        }
    }//GEN-LAST:event_chooseExcelDirButtonActionPerformed

